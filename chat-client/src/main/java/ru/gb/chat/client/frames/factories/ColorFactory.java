import java.awt.*;

/**
 * All colors which are used in project.
 */
public class ColorFactory {

    /**
     *
     * @return rgb(72, 72, 72)
     */
    public static Color getMain() {
        return new Color(72, 72, 72);
    }

    /**
     *
     * @return rgb(136, 136, 136)
     */
    public static Color getSecond() {
        return new Color(136, 136, 136);
    }

    /**
     *
     * @return rgb(169, 169, 169)
     */
    public static Color getButtonsColor() {
        return new Color(169, 169, 169);
    }

    /**
     *
     * @return rgb(246, 246, 246)
     */
    public static Color getOnePanelColor() {
        return new Color( 246, 246, 246);
    }
}
